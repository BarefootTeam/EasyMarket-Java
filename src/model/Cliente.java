package model;

/**
 *
 * @author Leo
 */
public class Cliente {
    
    private Long Id;
    private String Nome;
    private String Cpf;

    public Cliente() {
    }

   
    public Long getId() {
        return Id;
    }

    public void setId(Long Id) {
        this.Id = Id;
    }

    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }

    public String getCpf() {
        return Cpf;
    }

    public void setCpf(String Cpf) {
        this.Cpf = Cpf;
    }
    
    
}
